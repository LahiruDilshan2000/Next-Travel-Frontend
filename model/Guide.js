export class Guide {
    constructor(guideId,
                name,
                address,
                age,
                gender,
                contact,
                manDayValue,
                imageList) {
        this.guideId = guideId;
        this.name = name;
        this.address = address;
        this.age = age;
        this.gender = gender;
        this.contact = contact;
        this.manDayValue = manDayValue;
        this.imageList = imageList;
    }
}